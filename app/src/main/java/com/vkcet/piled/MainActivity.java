    private void Toggle(){

        String STATUS;

        if(tvStatus.getText().equals("OFF")) STATUS ="1";
        else STATUS="0";

        StringRequest stringRequest = new StringRequest(TWO+STATUS,
                new Response.Listener<String>() {
                    @Override
                    public void onResponse(String response) {
                        sendRequest();
                    }
                },
                new Response.ErrorListener() {
                    @Override
                    public void onErrorResponse(VolleyError error) {
                        Toast.makeText(MainActivity.this, error.getMessage(), Toast.LENGTH_LONG).show();
                    }
                });

        RequestQueue requestQueue = Volley.newRequestQueue(this);
        requestQueue.add(stringRequest);

    }

    private void sendRequest() {

        StringRequest stringRequest = new StringRequest(ONE,
                new Response.Listener<String>() {
                    @Override
                    public void onResponse(String response) {
                        try {
                            progressDialog.dismiss();
                            changeStatus(response);
                        } catch (JSONException e) {
                            e.printStackTrace();
                        }
                    }
                },
                new Response.ErrorListener() {
                    @Override
                    public void onErrorResponse(VolleyError error) {
                        Toast.makeText(MainActivity.this, error.getMessage(), Toast.LENGTH_LONG).show();
                    }
                });

        RequestQueue requestQueue = Volley.newRequestQueue(this);
        requestQueue.add(stringRequest);
    }

    private void changeStatus(String response) throws JSONException {

        JSONObject jsonObject = new JSONObject(response);
        String status = (String) jsonObject.get("a");
        if(status.equals("0")) {
            tvStatus.setText("OFF");
            imgLedon.setVisibility(View.GONE);
            imgLedoff.setVisibility(View.VISIBLE);
        }
        else{
            tvStatus.setText("ON");
            imgLedon.setVisibility(View.VISIBLE);
            imgLedoff.setVisibility(View.GONE);
        }


    }


